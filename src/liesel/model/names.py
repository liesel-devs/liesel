color_list = colors.lower().strip().split()
animals_list = animals.lower().strip().split()


def random_name() -> str:
    """
    Generates a random name using the pattern ``color-animal-uuid``.
    """
    color = random.choice(color_list)
    animal = random.choice(animals_list)
    suffix = uuid.uuid4().hex[:6]
    return color + "-" + animal + "-" + suffix
